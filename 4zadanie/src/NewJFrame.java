    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double[][] arr2 = new double[8][9];
        double  sum = 0;
        int kolvo = 0;
        
        //в этом цикле происходит подсчет суммы и количества
        for(int i = 0; i<8;i++)
            for(int j = 1; j<9; j++){
                arr1[i][j] = jTable1.getValueAt(i,j);
                if(i>j-1){
                    arr2[i][j] = Double.parseDouble((String) jTable1.getValueAt(i,j));
                    sum += arr2[i][j];
                    kolvo++;
                }
        }
        String summa = Double.toString(sum);
        String count = Double.toString(kolvo);
        jTextField1.setText(summa);
        jTextField2.setText(count);
    }//GEN-LAST:event_jButton1ActionPerformed

