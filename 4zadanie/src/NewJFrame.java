    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //заполнение ячеек пустыми значениями
        for (int i=0; i<8; i++){
            for (int j=0; j<9; j++)
                arr1[i][j] = "";
        }
        //заполнение первого столбца нумерации
        for (int i=0; i<8;i++)
            arr1[i][0] = i+1;
        for(int i = 0; i<8; i++)
            model.addRow(arr1[i]);
        
    }//GEN-LAST:event_jButton3ActionPerformed

